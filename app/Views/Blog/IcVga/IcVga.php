                                        sepele seperti pemasangan VGA yang tidak pas, melepas VGA pada saat komputer
                                        sedang menyala, meneteskan minuman ke laptop dalam kondisi casing terbuka, dan
                                        masih banyak lagi.</p>
                                </li>
