                                        di dalamnya. Ini bisa menyebabkan terjadinya overheat, dan overheat bisa
                                        menyebabkan fail-safe (lapto mati sendiri), bagian komponen meleleh, short
                                        circuit, dan bila terlalu ekstrim bisa menyebabkan VGA mati total.</p>
